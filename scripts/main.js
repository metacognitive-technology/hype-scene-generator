cloneSceneContainerAndSymbolHolder = (oldSC) => {
    //clone both the symbolHolder (with its elements) and its associated sceneContainer and return them
    var nSC = JSON.parse(JSON.stringify(oldSC))
    var oldSH = findSymbolHolderByOid(nSC.scenes[0].rootSymbolControllerOid)
    var nSH = JSON.parse(JSON.stringify(oldSH))
    var clonedElements = {}
    _.values(nSH.symbol.elements).forEach((el) => {
        clonedElements[nextOid()] = el
    })
    nSH.symbol.elements = clonedElements
    var nSHOid = nextOid()
    nSH.symbol.oid = nSHOid
    nSC.name = nSC.name //+ 'Q'
    var nSCOid = nextOid()
    var nSOid = nextOid()
    nSC.scenes[0].rootSymbolControllerOid = nSHOid
    nSC.scenes[0].oid = nSOid
    nSC.oid = nSCOid
    nSC.currentSceneOid = nSOid
    return {symbolHolder:nSH,sceneContainer:nSC}
}

cloneSceneContainerAndSymbolHolderWithNewBackgroundImage = (oldSC,imageFilename) => {
    var results = cloneSceneContainerAndSymbolHolder(oldSC)
    var newR = newResource(imageFilename)
    plistJSON.resourcesInfo.resources.push(newR)
    var newG = newGroup([newR.resourceName],newR.resourceName.split('.png')[0])
    plistJSON.resourcesInfo.groups.push(newG)
    var newImageElementPair = _.pairs(newImageElement(newG.oid,newG.name))[0]
    results.symbolHolder.symbol.elements[newImageElementPair[0]] = newImageElementPair[1]
    results.sceneContainer.name = newG.name
    return results
}


