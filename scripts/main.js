cloneScene = () => {
    if (currentSceneContainer !== null) {
        var results = cloneSceneContainerAndSymbolHolder(currentSceneContainer)
        var scIndex = _.indexOf(plistJSON.sceneContainers,currentSceneContainer)
        var before = _.first(plistJSON.sceneContainers,scIndex)
        var after = _.rest(plistJSON.sceneContainers,scIndex+1)
        plistJSON.sceneContainers = _.union(before,[results.sceneContainer],after)
        plistJSON.symbols.push(results.symbolHolder)
        updateSceneList()
    }
}
